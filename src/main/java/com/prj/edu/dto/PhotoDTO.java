package com.prj.edu.dto;

public class PhotoDTO {
	private int photo_idx;
	private String photo_original;
	private String photo_copy;
	private int photo_pr_num;
	private String photo_category;
	public int getPhoto_idx() {
		return photo_idx;
	}
	public void setPhoto_idx(int photo_idx) {
		this.photo_idx = photo_idx;
	}
	public String getPhoto_original() {
		return photo_original;
	}
	public void setPhoto_original(String photo_original) {
		this.photo_original = photo_original;
	}
	public String getPhoto_copy() {
		return photo_copy;
	}
	public void setPhoto_copy(String photo_copy) {
		this.photo_copy = photo_copy;
	}
	public int getPhoto_pr_num() {
		return photo_pr_num;
	}
	public void setPhoto_pr_num(int photo_pr_num) {
		this.photo_pr_num = photo_pr_num;
	}
	public String getPhoto_category() {
		return photo_category;
	}
	public void setPhoto_category(String photo_category) {
		this.photo_category = photo_category;
	}
	
	
	

}
